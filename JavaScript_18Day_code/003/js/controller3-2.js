//赋值运算符
// = 

// += -= *= /= %=
var iNum = 5;
iNum += 15;//iNum = iNum + 15;
document.write('[+=] iNum = '+ iNum + '<br />');//20

iNum = 5;
iNum -= 15;//iNum = iNum - 15;
document.write('[-=] iNum = '+ iNum + '<br />');//-10

iNum = 5;
iNum *= 3;//iNum = iNum * 3;
document.write('[*=] iNum = '+ iNum + '<br />');//15

iNum = 5;
iNum /= 2;//iNum = iNum / 2;
document.write('[/=] iNum = '+ iNum + '<br />');//2.5

iNum = 5;
iNum %= 3;//iNum = iNum % 3;
document.write('[%=] iNum = '+ iNum + '<br />');//2

// 逗号,是表达式，其以最后一个值返回


